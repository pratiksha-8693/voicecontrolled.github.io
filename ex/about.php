<h2 class="display-7">Voice Controlled Home Automation System</h2>
<p class="py-5">
A fully autonomous home is going to be the future. Everyone want that the complete control such as switching on/off the 
lights, fans, televisions and other appliances through one device. This paper presents a novel solution that makes this future a bit nearer. 
Voice controlled automation, as well as a universal remote control application on an Android platform, has been presented. 
The efficient utilization of various available apps on Google play store has been done. 
Using the Bluetooth module HC-05, Arduino Uno, and Android application, voice controlled automation, as well as the universal switch, has been realized.

The primary objective of our project is to construct a fully functional voice based Home automation system 
that uses Internet of Things, Artificial Intelligence and Natural Language Processing (NLP) to provide a cost-effective, efficient way to work together with home appliances. 
There are many smart home solutions in the market that aim to automate the basic operations of these home appliances using 
various technologies such as GSM (Global System for Mobile), NFC (Near-Field Communication) etc. However,
most of these systems focus on mimicking the basic operation of the electrical switch. 
Our project aims at providing a fully automated voice based solution that our users can rely on,
to perform more than just switching on/off the appliances. 
The user sends a command through speech to the mobile device,
which interprets the message and sends the appropriate command to the specific appliance. 
We plan on implementing four basic home appliances as a “Proof-of-Concept” for this project which includes Fan, Light, Coffee Machine and Door Alarms. 
The voice command given by the user is interpreted by the mobile device using Natural Language processing. 
The mobile device acts as a central console; it determines what operation must be completed by which appliance to fulfill the user's request.
The central console might likewise be either a desktop application, web application or a smart phone application as nearly all of the data transferred can be processed by the cloud.
However, for the convenience of the user and increased mobile capabilities we will be using a smart phone in this project. 
The appliances are associated with the mobile device through an Arduino Board that establishes the concept of Internet of Things.
The Arduino Boards are interfaced with the appliances and programmed in a manner that they respond to mobile inputs.
